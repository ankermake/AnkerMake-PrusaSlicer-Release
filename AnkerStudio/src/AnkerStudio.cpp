std::string string_To_UTF8(const std::string& str)
{
    std::string strCodePage = boost::locale::util::get_system_locale();
    std::locale loc = boost::locale::generator().generate(strCodePage);
    auto dataStyle = use_facet<boost::locale::info>(loc).encoding();
    std::string readlData = boost::locale::conv::between((char*)str.c_str(), (char*)str.c_str() + str.size(), dataStyle, "UTF8");
    return readlData;
}

std::string format_message(
    boost::log::value_ref< std::string, boost::log::expressions::tag::smessage > 
    const& message)
{    
    static bool bFlag = false;    
    std::string msg = std::string();
    if (message)
    {
        msg = message.get();
        if (bFlag)
        {
            std::string plaintValue(msg);
            int strLength = 0;
            plaintValue += '\n';
  
            plaintValue = string_To_UTF8(plaintValue);
            std::string ciphertext = std::string();

#if defined(_DEBUG) || !defined(NDEBUG)
            ciphertext = plaintValue;
#else
            logCryptText(plaintValue, strLength, ciphertext);
#endif

            return ciphertext;
        }
        else
        {
            bFlag = true;
            return msg;
        }
        
        return msg;
    }
    
    return std::string();
}

